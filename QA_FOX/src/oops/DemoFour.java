package oops;

public class DemoFour {
	
	//Constructors

	public  DemoFour(int a, int b) {
		System.out.println("The sum of "+a+" and "+b+" is: "+(a+b));
	}
	
	public  DemoFour(int a, int b,int c) {
		System.out.println("The sum of "+a+" and "+b+" and "+c +" is: "+(a+b+c));
	}
	
	public  DemoFour(int a, double b) {
		System.out.println("The sum of "+a+" and "+b+" is: "+(a+b));
	}
	
	public  DemoFour(double a, int b) {
		System.out.println("The sum of "+a+" and "+b+" is: "+(a+b));
	}
}
